class Vehicle {
    /**
     * Class to keep track of vehicle objects.
     * @param {*} make brand of vehicle
     * @param {*} model vehicle model
     * @param {*} year vehicle year
     * @param {*} size Car/Van
     */
    constructor(make, model, year, size) {
        self.make = make;
        self.model = model;
        self.year = year;
        self.size = size;
    }
}